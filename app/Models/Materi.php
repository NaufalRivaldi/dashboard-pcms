<?php
// ------------------------------------------------------------------------------------
namespace App\Models;
// ------------------------------------------------------------------------------------
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;
// ------------------------------------------------------------------------------------
class Materi extends Model
{
    // --------------------------------------------------------------------------------
    use SoftDeletes;
    // --------------------------------------------------------------------------------
    protected $table = 'materi';
    // --------------------------------------------------------------------------------
    protected $fillable = ['nama', 'status', 'kategori_id'];
    // --------------------------------------------------------------------------------
    public $timestamps = false;
    // --------------------------------------------------------------------------------

    // --------------------------------------------------------------------------------
    // Set relationship
    // --------------------------------------------------------------------------------
    public function materi_grades(){
        return $this->hasMany(MateriGrade::class, 'materi_id');
    }
    // --------------------------------------------------------------------------------
    public function kategori(){
        return $this->belongsTo(Kategori::class, 'kategori_id')->withTrashed();
    }
    // --------------------------------------------------------------------------------
    public function siswa_aktif_details(){
        return $this->hasMany(SiswaAktif::class, 'materi_id');
    }
    // --------------------------------------------------------------------------------
}
// ------------------------------------------------------------------------------------